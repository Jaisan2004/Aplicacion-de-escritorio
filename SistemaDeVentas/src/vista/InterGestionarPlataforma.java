    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        if(!txt_estado.getText().isEmpty()){
            Plataforma plataforma = new Plataforma();
            Ctrl_Plataforma controlPlataforma = new Ctrl_Plataforma();
            
            plataforma.setDescripcion(txt_descripcion.getText().trim());
            plataforma.setEstado(Integer.parseInt(txt_estado.getText().trim()));
            if(txt_descripcion.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Ingrese una descripcion para guardar");
                txt_descripcion.setBackground(Color.red);
            }else{
                if(controlPlataforma.Actualizar(plataforma, idPlataforma)){
                    JOptionPane.showMessageDialog(null,"Plataforma Actualizada");
                    txt_descripcion.setText("");
                    txt_descripcion.setBackground(Color.white);
                    txt_estado.setText("");
                    this.CargarTablaPlataformas();
                }else{
                    JOptionPane.showMessageDialog(null, "Error al actualizar la Plataforma");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una Plataforma");
        }
    }//GEN-LAST:event_jButton_actualizarActionPerformed

