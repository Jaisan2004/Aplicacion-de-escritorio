    private void CargarTablaPlataformas(){
        Connection con = (Connection) Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql="select idPlataforma, descripcion, estado from "+TABLA;
        Statement st;
        
        try {
            st =(Statement) con.createStatement();
            ResultSet rs= st.executeQuery(sql);
            InterGestionarPlataforma.jTable_plataformas = new JTable(model);
            InterGestionarPlataforma.jScrollPane1.setViewportView(InterGestionarPlataforma.jTable_plataformas);
            
            model.addColumn("idPlataforma");
            model.addColumn("descripcion");
            model.addColumn("estado");
            
            while(rs.next()){
                Object fila[] = new Object[3];
                
                for(int i = 0; i<3; i++){
                    fila[i] = rs.getObject(i+1);
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llamar la tabla plataformas: "+e);
        }
        
        jTable_plataformas.addMouseListener(new MouseAdapter(){
           @Override
           public void mouseClicked(MouseEvent e){
               int fila_point =jTable_plataformas.rowAtPoint(e.getPoint());
               int columna_point = 0;
               if(fila_point > -1){
                   idPlataforma = (int) model.getValueAt(fila_point, columna_point);
                   EnviarDatosCategoriaSeleccionada(idPlataforma);
               }
           }  
        });
    }
    
    private void EnviarDatosCategoriaSeleccionada(int idPlataforma){
        try {
            Connection con = (Connection) Conexion.conectar();
            PreparedStatement pat = (PreparedStatement) con.prepareStatement(
                    "select * from "+TABLA+" WHERE idPlataforma = '"+idPlataforma+"';");
            ResultSet rs = pat.executeQuery();
            
            if(rs.next()){
                txt_descripcion.setText(rs.getString("descripcion"));
                txt_estado.setText(rs.getString("estado"));
            }
            con.close();
        } catch (Exception e) {
            System.out.println("Error al seleccionar Plataforma: "+ e);
        }        
    }
    
